print("for i in range(10): ")
for i in range(10):
    print(i, end=' ')
print("\n")

print("for i in range(1, 10,): ")
for j in range(1, 10, 2):
    print(j, end=' ')
print("\n")

print("for k in 'Hello word': ")
for k in "Hello word":
    l = k * 3
    print(l, end=', ')
print("\n")

print("for l in [1,2,3,4,5]: ")
for l in [1,2,3,4,5]:
    m = l % 2
    print(m, end=' = m % 2\n')
print("\n")

